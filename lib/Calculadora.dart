

class Calculadora{

  int soma({ int valorA, int valorB = -1 }){
    return valorA + valorB;
  }

  int subtracao({ int valorA, int valorB = -1 }){
    return valorA - valorB;
  }

  int multiplicacao({ int valorA, int valorB = -1 }){
    return valorA * valorB;
  }

  double divizao({ int valorA, int valorB = -1 }){
    return valorA / valorB;
  }



}